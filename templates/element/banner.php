    <h3>HIGH RISE LIVING AND THE INCLUSIVE CITY </h3>
        <p> Exerpts from the project’s presentation on the french blog, to be re-written:

            «What capacity is left to produce an inclusive city, in the context of an accelerated verticalization of contem-
            porary metropolises? At first sight, promoting a denser city would accommodate further growth, provide

            housing for the people while limiting urban sprawl. That is how densification is justified in many cities across
            the world. But in a context where regulatory capitalism and entrepreneurial municipalities are participating
            in the creation of the most favorable conditions for developers to control the urban space, verticalization,
            and particularly residential verticalization is questionable. If office towers have gained attention recently,
            the vast majority of vertical developments are residential, a phenomenon having far reaching consequences
            on the daily life of residents and urban communities. Verticalization, if not new, is currently happening in a

            very different context than after 1945, when Charte d’Athènes and modernism where the dominant plan-
            ning principles. Today, residential high-rises are more than architectural solutions, as much as office towers,

            they are commodities in a global market where capital flows are fixed by developers and municipalities. By
            shedding a light on the making and the experience of residential high-rises, we assess the contemporary
            transformation of the city and test it against inclusiveness. Aiming at the construction of a transdisciplinary
            theoretical framework (architecture, geography, sociology, anthropology), we intend to critically question
            the inclusiveness of contemporary urban production, through the residential high-rise phenomenon, in Lyon
            and Sao Paulo. Inclusiveness, as defined by the UN (economic; social; political; cultural and symbolical1) is
            our benchmark, both a state and a process; it is a result and a condition for an egalitarian/equitable urban
            environment. It lies in the making and in the experience and imaginary of the city.

            Framed by the above theoretical framework, the project is rooted in case studies, enabling to reveal diffe-
            rent types of local-global negotiations in the making of the city. In order to deliver the most, Lyon and Sao

            Paulo where the partners of the project are based (USP and Université Lyon2, with collaborations with
            practitioners such as the Municipal Agencies of both cities), have been selected as the core case studies,
            because of their specific residential high-rise history. The project is thus both international (experts from
            abroad will be invited) and deeply locally rooted.» </p>
